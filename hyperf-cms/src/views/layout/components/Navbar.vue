            <search id="header-search" class="right-menu-item" />
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
  </div>
</template>
<script>
import { mapGetters } from 'vuex'
import Breadcrumb from './Breadcrumb'
import TopNav from './TopNav'
import Hamburger from './Hamburger'
import Search from './HeaderSearch'
import Screenfull from './Screenfull'
import ProjectLink from './ProjectLink'
import LayoutSetting from './LayoutSetting'
import Maintain from './Maintain'
export default {
  components: {
    Breadcrumb,
    Hamburger,
    Search,
    Screenfull,
    ProjectLink,
    LayoutSetting,
    TopNav,
    Maintain,
  },
  computed: {
    ...mapGetters(['sidebar', 'avatar']),
    device() {
      return this.$store.state.app.device
    },
    topNav: {
      get() {
        return this.$store.state.setting.topNav
      },
    },
    simpleMaintainSwitch: {
      get() {
        return this.$store.state.app.simple_maintain_switch
      },
    },
  },
  data() {
    return {
      personalDialogVisible: false,
      noticeText: '',
    }
  },
  created() {},
  methods: {
    toggleSideBar() {
      //Expand and collapse the sidebar
      this.$store.dispatch('ToggleSideBar')

      if (this.device == 'mobile') {
        let isOpen = this.sidebar.opened
        if (isOpen) {
          document.body.style.overflow = 'hidden'
        } else {
          document.body.style.overflow = ''
        }
      }
    },
    logout() {
      this.$store.dispatch('LogOut').then(() => {
        location.reload() //In order to re-instantiate the vue-router object to avoid bugs
      })
    },
    getDownloadUrl() {
      this.$alert(this.$store.getters.downLoadUrl, 'download link', {
        confirmButtonText: 'Sure',
      })
    },
    handleCopy(text, event) {
      clip(text, event)
    },
    isMobile() {
      //Determine whether the mobile terminal
      let flag = navigator.userAgent.match(
        /(phone|pad|pod|iPhone|iPod|ios|iPad|Android|Mobile|BlackBerry|IEMobile|MQQBrowser|JUC|Fennec|wOSBrowser|BrowserNG|WebOS|Symbian|Windows Phone)/i
      )
      return flag
    },
    personal() {
      this.$router.push({
        path: '/profile',
      })
    },
    advice() {
      this.$router.push({ path: '/setting/system_module/advice' })
    },
  },
}
</script>
